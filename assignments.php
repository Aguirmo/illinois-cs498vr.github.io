<h3><a name="late">Late submission policy</a></h3>
20% will be automatically deducted if you submit your assignment within the next two days past the deadline. We will not accept any submissions after two days past the deadline.

<hr />
<h3><a name="regrades">Regrade requests</a></h3>

<ul>
	<li>If you believe that your assignment or exam has been graded unfairly, please request a regrade.</li>
	<li>MPs will be regraded by the TAs. To request a regrade for an MP, create a private Piazza post with a brief explanation why you think you were graded unfairly within the week after grades come out. Please <strong>include yours and your partner's NetIDs</strong>. In the subject line indicate that it is a regrade request together with the corresponding assignment number. (That is, "Regrade request for MP1"). Due to the significant amount of time grading MPs takes, TAs will most likely process the regrades at the end of the semester only. They will only process requests for those students whose final grades would depend on the outcome of the regrade.</li>
	<li>Exams will be regraded by the instructor. For an exam regrade request - you have to submit the regrade request on paper - this can not be handled electronically. You have to submit a printed justification for your regrade request in person to the instructor during office hours. Handling the exam regrade request might take up to two weeks.</li>
	<li>Regrade requests must be submitted at most <strong> one week</strong> after the MP or exam grades are returned. Late regrade requests will be ignored.</li>
	<li>If you submit a regrade request, your entire MP or exam will be regraded from scratch. Your grade may go down.
