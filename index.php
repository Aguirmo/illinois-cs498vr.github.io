<h3>Overview</h3>

<p>Virtual reality (VR) technology transports us to real or synthetic places that may be inaccessible, breathtaking, complex beyond our wildest imagination, or just simple and relaxing. Applications include entertainment, social interaction, virtual travel, remote training, architectural walkthroughs, cultural appreciation, and learning enhancement.</p>

<p>Although VR has been around for decades, it always came with a high cost to enter the field because of advanced, expensive equipment and computing resources. Thanks to widespread progress in display, sensing, and computational technology, the newest VR systems are cheap, lightweight, and easy to program. This has caused a flood of excitement as almost anyone can pick up a VR headset and start developing experiences.</p>

<h3>Course Objectives</h3>

<p>The purpose of this course is to provide students with both a deep understanding of the fundamentals of VR and to gain practical experience. Because VR tricks our brains by presenting synthetic stimuli to our senses, it is extremely challenging to develop and analyze VR systems that are both effective and comfortable. To get a handle on these issues, this course will fuse together knowledge from a variety of relevant topics, including computer graphics, tracking systems, and perceptual psychology. Some basic questions that motivate the course topics:</p>

<ul>
	<li>How does one build a good VR experience?</li>
	<li>How do VR systems work using current technology?</li>
	<li>What is wrong with current systems?</li>
	<li>How does the human body respond to VR?</li>
	<li>What fundamentals could help you to shape the future?</li>
</ul>

<h3>Course Topics</h3>

<ul>
	<li>Overview and perspective on virtual reality</li>
	<li>Human sensation and perception</li>
	<li>Engineering VR systems</li>
	<li>Perceptual training</li>
	<li>Building good experiences</li>
</ul>

<h3>Prerequisites</h3>

<p>This is a large, experimental course on a popular topic. We expect students to come with a variety backgrounds and we do not demand they have had particular background courses. It is important for you assess whether you can handle the material. You can start by looking at the textbooks. The two most challenging aspects will be comfort with: 1) basic linear algebra, at least with respect to 3D transformations, and 2) programming on C++ or related languages. If you are not strongly skilled in these aspects, then you might have some burden to catch up. CS 225 and Math 415 would be sufficient, but are not necessary if you have other background or an inclination to learn quickly. Ask us if you are unsure.</p>

