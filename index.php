<h3>Assignments</h3>

<p>We expect to have 4 assignments over the course of the semester, in addition to a final project. In each of these, students will work in pairs to solve a specific implementation problem in the virtual reality lab in 4107 Siebel Center. The lab is equipped with powerful Falcon Northwest PCs that have dual 27-inch monitors, NVidia GeForce Titan Black graphics cards with 6GB of RAM, and Windows 10 running Visual Studio. The VR equipment includes an Oculus Rift CV1 for every PC, which were kindly donated by Oculus/Facebook. Each assignment could also include a written part, which involves solving mathematical problems.</p>

<p>You may also consider doing development outside of the lab machines; however, to experience and evaluate the solution correctly, it is crucial to run the code at least once on the lab systems. They have been optimized for Oculus Rift CV1 experiences. Without using the 4107 machines, there may be issues that severely degrade performance, leading to a bad VR experience.</p>

<p>Unfortunately, we have only 20 workstations and over 200 students. Therefore, it is important to start early on assignments and try to work in the lab during less popular times. The 4107 lab is generally available any time.</p>

<p>For our assignments class policies, see <a href="assignments.php#submit">Assignments page</a>.</p>

